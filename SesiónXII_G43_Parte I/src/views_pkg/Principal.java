    void listar(){
        String sql = "SELECT * FROM tb_persons";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            //Los datos que devuelve la consulta se muestren en la tabkla
            Object[]person = new Object[3];
            modelo = (DefaultTableModel)tbl_persons.getModel();
            while(rs.next()){
                person[0] = rs.getInt("id");
                person[1] = rs.getInt("dni");
                person[2] = rs.getString("nombre");
                modelo.addRow(person);
                System.out.println(rs.getInt("id"));
            }
            tbl_persons.setModel(modelo);
        }catch(Exception e){
            
        }
    }

