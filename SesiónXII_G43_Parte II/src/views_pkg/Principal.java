    void listar(){
        String sql = "SELECT * FROM tb_persons";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            //Los datos que devuelve la consulta se muestren en la tabkla
            Object[]person = new Object[3];
            modelo = (DefaultTableModel)tb_data.getModel();
            while(rs.next()){
                person[0] = rs.getInt("id");
                person[1] = rs.getInt("dni");
                person[2] = rs.getString("nombre");
                modelo.addRow(person);
                System.out.println(rs.getInt("id"));
            }
            tb_data.setModel(modelo);
        }catch(SQLException e){
            
        }
    }
    
    void add_user(){
        int dni = Integer.parseInt(txt_dni.getText());
        String name = txt_name.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del usuario");
        }else{
            String query = "INSERT INTO tb_persons(dni, nombre) VALUES (" + dni + ", '"+name + "')";
            //INSERT INTO tb_persons(dni, nombre) VALUES (dni, 'name');
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El usuario ha sido creado");
                clear_rows_table();
                listar();
            }catch(HeadlessException | SQLException e){
              JOptionPane.showMessageDialog(this, "No se pudo crear el usuario");
            }
        }   
    }
    
    
    void edit_user(){
        //Hacemos nuevamente lectura de los valores contenidos en los JTextField
        //Para identificar si el usuario modifico algÃºn valor
        int id = Integer.parseInt(txt_id.getText());
        int dni = Integer.parseInt(txt_dni.getText());
        String name = txt_name.getText();
        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Falta ingresar el nombre del usuario");
        }else{
            String query = "UPDATE tb_persons SET dni = " + dni + ", nombre= '"+name + "' WHERE id = " + id;
            //UPDATE tb_persons SET dni =dni, nombre= 'name' WHERE id = id
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El usuario ha sido modificado con exito");
                clear_rows_table();
                listar();
            }catch(HeadlessException | SQLException e){
              JOptionPane.showMessageDialog(this, "No se pudo crear el usuario");
            }
        }   
    }
    
    
    void delete_user(){
        int fila = tb_data.getSelectedRow();
        int id = Integer.parseInt(txt_id.getText());
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "No has seleccionado un usuario");
        }else{
            
            System.out.println("ID: " + id);
            String query = "DELETE FROM tb_persons WHERE id = " + id;
            try{
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "El usuario ha sido eliminado con exito");
                clear_rows_table();
                listar();
            }catch(HeadlessException | SQLException e){
            }
        }
    }
